DROP TABLE ssm;
CREATE TABLE ssm (
  icgc_mutation_id VARCHAR2(255) DEFAULT NULL,
  icgc_donor_id VARCHAR2(255) DEFAULT NULL,
  project_code VARCHAR2(255) DEFAULT NULL,
  icgc_specimen_id VARCHAR2(255) DEFAULT NULL,
  icgc_sample_id VARCHAR2(255) DEFAULT NULL,
  matched_icgc_sample_id VARCHAR2(255) DEFAULT NULL,
  submitted_sample_id VARCHAR2(255) DEFAULT NULL,
  submitted_matched_sample_id VARCHAR2(255) DEFAULT NULL,
  chromosome VARCHAR2(255) DEFAULT NULL,
  chromosome_start NUMBER(10) DEFAULT NULL,
  chromosome_end NUMBER(10) DEFAULT NULL,
  chromosome_strand NUMBER(10) DEFAULT NULL,
  assembly_version VARCHAR2(255) DEFAULT NULL,
  mutation_type VARCHAR2(255) DEFAULT NULL,
  reference_genome_allele VARCHAR2(255) DEFAULT NULL,
  mutated_from_allele VARCHAR2(255) DEFAULT NULL,
  mutated_to_allele VARCHAR2(255) DEFAULT NULL,
  quality_score VARCHAR2(255) DEFAULT NULL,
  probability decimal(7,6) DEFAULT NULL,
  total_read_count NUMBER(10) DEFAULT NULL,
  mutant_allele_read_count NUMBER(10) DEFAULT NULL,
  verification_status VARCHAR2(255) DEFAULT NULL,
  verification_platform VARCHAR2(255) DEFAULT NULL,
  biological_validation_status VARCHAR2(255) DEFAULT NULL,
  biological_validation_platform VARCHAR2(255) DEFAULT NULL,
  consequence_type VARCHAR2(255) DEFAULT NULL,
  aa_mutation VARCHAR2(255) DEFAULT NULL,
  cds_mutation VARCHAR2(255) DEFAULT NULL,
  gene_affected VARCHAR2(255) DEFAULT NULL,
  transcript_affected VARCHAR2(225) DEFAULT NULL,
  gene_build_version NUMBER(10) DEFAULT NULL,
  platform VARCHAR2(255) DEFAULT NULL,
  experimental_protocol VARCHAR2(255) DEFAULT NULL,
  sequencing_strategy VARCHAR2(255) DEFAULT NULL,
  base_calling_algorithm VARCHAR2(255) DEFAULT NULL,
  alignment_algorithm VARCHAR2(225) DEFAULT NULL,
  variation_calling_algorithm VARCHAR2(255) DEFAULT NULL,
  other_analysis_algorithm VARCHAR2(255) DEFAULT NULL,
  seq_coverage VARCHAR2(255) DEFAULT NULL,
  raw_data_repository VARCHAR2(255) DEFAULT NULL,
  raw_data_accession VARCHAR2(255) DEFAULT NULL,
  initial_data_release_date VARCHAR2(255) DEFAULT NULL
);
